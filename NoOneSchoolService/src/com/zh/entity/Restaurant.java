package com.zh.entity;

import com.zh.entity.common.Entity;

public class Restaurant extends Entity{
	private String image;
	private String name;
	private String address;
	private String iphone;
	private int noodle;
	private int setmeal;
	private int rice;
	private int snack;
	private int barbecue;
	private int congee;
	
	
	public String getImage() {
		return image;
	}
	public void setImage(String image) {
		this.image = image;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getIphone() {
		return iphone;
	}
	public void setIphone(String iphone) {
		this.iphone = iphone;
	}
	public int getNoodle() {
		return noodle;
	}
	public void setNoodle(int noodle) {
		this.noodle = noodle;
	}
	public int getSetmeal() {
		return setmeal;
	}
	public void setSetmeal(int setmeal) {
		this.setmeal = setmeal;
	}
	public int getRice() {
		return rice;
	}
	public void setRice(int rice) {
		this.rice = rice;
	}
	public int getSnack() {
		return snack;
	}
	public void setSnack(int snack) {
		this.snack = snack;
	}
	public int getBarbecue() {
		return barbecue;
	}
	public void setBarbecue(int barbecue) {
		this.barbecue = barbecue;
	}
	public int getCongee() {
		return congee;
	}
	public void setCongee(int congee) {
		this.congee = congee;
	}
	
	
}
